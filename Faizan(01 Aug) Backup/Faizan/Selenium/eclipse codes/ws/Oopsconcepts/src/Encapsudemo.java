
public class Encapsudemo {
	
	private int age;
	private String name;
	
	public int getage()
	{
	
		
		return age;
		
	}
	
	public String getname(){
		return name;
	}

	public void setage(int value){
		age = value;		
		
	}
	public void setname(String value){
		name = value;
		
	}
}
	


